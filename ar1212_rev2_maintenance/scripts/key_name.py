from enum import IntEnum

class KeyName(IntEnum):
    """KeyName enumeration
    """
    KRTL = 0             # Key from RTL
    KPICV = 1            # Key from Chip Manufacturer
    KCP = 2              # Key from Device Manufacturer
    ROT_CM_PAIR = 3      # Chip Manufacturer Root-Of-Trust assymetric key pair (HBK0)
    ROT_DM_PAIR = 4      # Device Manufacturer Root-Of-Trust assymetric key pair (HBK1)
    SENSOR_PAIR = 5      # sensor session/signing assymetric key pair
    VENDOR_PAIR = 6      # vendor signing assymetric key pair


